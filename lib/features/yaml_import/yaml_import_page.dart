// ── Result view ────────────────────────────────────────────────────────────

class _ResultView extends StatefulWidget {
  final YamlImportResult result;
  final VoidCallback onReset;
  final VoidCallback onCommit;
  const _ResultView({
    required this.result,
    required this.onReset,
    required this.onCommit,
  });

  @override
  State<_ResultView> createState() => _ResultViewState();
}

class _ResultViewState extends State<_ResultView> {
  @override
  Widget build(BuildContext context) {
    final r = widget.result;
    final canCommit = r.validGoals.isNotEmpty ||
        r.conflictGoals.any((g) => !g.skipOnConflict);

    return Padding(
      padding: const EdgeInsets.fromLTRB(
          AppSpacing.xl, 0, AppSpacing.xl, AppSpacing.xl),
      child: Column(
        children: [
          // Summary row
          _SummaryRow(
            valid: r.validGoals.length,
            conflicts: r.conflictGoals.length,
            errors: r.errors.length,
          ),

          const SizedBox(height: 12),

          Expanded(
            child: ListView(
              children: [
                // Errors
                if (r.errors.isNotEmpty) ...[
                  Text('ERRORS', style: AppTypography.sectionHeader),
                  const SizedBox(height: 6),
                  ...r.errors.map((e) => _ErrorCard(message: e)),
                  const SizedBox(height: 16),
                ],

                // Valid goals
                if (r.validGoals.isNotEmpty) ...[
                  Text('VALID GOALS', style: AppTypography.sectionHeader),
                  const SizedBox(height: 6),
                  ...r.validGoals.map((g) => _GoalCard(
                        name: g.name,
                        tasks: g.tasks.length,
                        badge: 'New',
                        badgeColor: AppColors.accentBlue,
                        trailingWidget: const Icon(Icons.check_circle_outline, color: AppColors.success, size: 18),
                      )),
                  const SizedBox(height: 16),
                ],

                // Conflicts
                if (r.conflictGoals.isNotEmpty) ...[
                  Text('CONFLICTS — Goal IDs already in your database', style: AppTypography.sectionHeader),
                  const SizedBox(height: 6),
                  ...r.conflictGoals.map((g) => _ConflictCard(
                        goalData: g,
                        onSkipChanged: (skip) {
                          setState(() => g.skipOnConflict = skip);
                        },
                      )),
                  const SizedBox(height: 16),
                ],
              ],
            ),
          ),

          // Action buttons
          Row(
            children: [
              Expanded(
                child: OutlinedButton(
                  onPressed: widget.onReset,
                  child: const Text('Edit YAML'),
                ),
              ),
              const SizedBox(width: 12),
              Expanded(
                child: ElevatedButton(
                  onPressed: canCommit ? widget.onCommit : null,
                  child: const Text('Import Goals'),
                ),
              ),
            ],
          ),
        ],
      ),
    );
  }
}

class _SummaryRow extends StatelessWidget {
  final int valid;
  final int conflicts;
  final int errors;
  const _SummaryRow({required this.valid, required this.conflicts, required this.errors});

  @override
  Widget build(BuildContext context) {
    return Row(
      children: [
        _SummaryBadge('$valid valid', AppColors.success),
        const SizedBox(width: 8),
        if (conflicts > 0) _SummaryBadge('$conflicts conflict${conflicts > 1 ? 's' : ''}', AppColors.warning),
        if (conflicts > 0) const SizedBox(width: 8),
        if (errors > 0) _SummaryBadge('$errors error${errors > 1 ? 's' : ''}', AppColors.accentRed),
      ],
    );
  }
}

class _SummaryBadge extends StatelessWidget {
  final String label;
  final Color color;
  const _SummaryBadge(this.label, this.color);

  @override
  Widget build(BuildContext context) => Container(
        padding: const EdgeInsets.symmetric(horizontal: 10, vertical: 5),
        decoration: BoxDecoration(
          color: color.withValues(alpha: 0.15),
          borderRadius: AppRadius.chip,
          border: Border.all(color: color.withValues(alpha: 0.4)),
        ),
        child: Text(label,
            style: AppTypography.badge.copyWith(color: color, fontSize: 11)),
      );
}

