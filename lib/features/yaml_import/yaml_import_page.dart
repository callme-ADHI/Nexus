  });

  @override
  Widget build(BuildContext context) => Container(
        margin: const EdgeInsets.only(bottom: 8),
        padding: const EdgeInsets.all(AppSpacing.lg),
        decoration: BoxDecoration(
          color: AppColors.surface,
          borderRadius: AppRadius.card,
          border: Border.all(color: AppColors.border),
        ),
        child: Row(
          children: [
            Expanded(
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(name, style: AppTypography.cardTitle),
                  Text('$tasks task${tasks == 1 ? '' : 's'}', style: AppTypography.caption),
                ],
              ),
            ),
            Container(
              padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 3),
              decoration: BoxDecoration(
                color: badgeColor.withValues(alpha: 0.15),
                borderRadius: AppRadius.chip,
              ),
              child: Text(badge,
                  style: AppTypography.badge.copyWith(color: badgeColor, fontSize: 10)),
            ),
            if (trailingWidget != null) ...[
              const SizedBox(width: 8),
              trailingWidget!,
            ],
          ],
        ),
      );
}

class _ConflictCard extends StatelessWidget {
  final YamlGoalData goalData;
  final ValueChanged<bool> onSkipChanged;
  const _ConflictCard({required this.goalData, required this.onSkipChanged});

  @override
  Widget build(BuildContext context) => Container(
        margin: const EdgeInsets.only(bottom: 8),
        padding: const EdgeInsets.all(AppSpacing.lg),
        decoration: BoxDecoration(
          color: AppColors.surface,
          borderRadius: AppRadius.card,
          border: Border.all(color: AppColors.warning.withValues(alpha: 0.4)),
        ),
        child: Row(
          children: [
            Expanded(
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(goalData.name, style: AppTypography.cardTitle),
                  Text('ID already exists', style: AppTypography.caption.copyWith(color: AppColors.warning)),
                ],
              ),
            ),
            Column(
              crossAxisAlignment: CrossAxisAlignment.end,
              children: [
                GestureDetector(
                  onTap: () => onSkipChanged(false),
                  child: _ConflictOption(
                    label: 'Overwrite',
                    selected: !goalData.skipOnConflict,
                    color: AppColors.accentRed,
                  ),
                ),
                const SizedBox(height: 4),
                GestureDetector(
                  onTap: () => onSkipChanged(true),
                  child: _ConflictOption(
                    label: 'Skip',
                    selected: goalData.skipOnConflict,
                    color: AppColors.textSecondary,
                  ),
                ),
              ],
            ),
          ],
        ),
      );
}

class _ConflictOption extends StatelessWidget {
  final String label;
  final bool selected;
  final Color color;
  const _ConflictOption({required this.label, required this.selected, required this.color});

  @override
  Widget build(BuildContext context) => AnimatedContainer(
        duration: const Duration(milliseconds: 150),
        padding: const EdgeInsets.symmetric(horizontal: 10, vertical: 4),
        decoration: BoxDecoration(
          color: selected ? color.withValues(alpha: 0.15) : Colors.transparent,
          borderRadius: AppRadius.chip,
          border: Border.all(
            color: selected ? color : AppColors.border,
          ),
        ),
        child: Text(label,
            style: AppTypography.badge.copyWith(
                color: selected ? color : AppColors.textSecondary, fontSize: 11)),
      );
}
